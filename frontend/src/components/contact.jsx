function Contact() {
  const formRef = useRef(null);
  const [loading, setLoading] = useState(false);
  const [message, setMessage] = useState("");

  const handleAnimatedSubmit = async (e) => {
    // e.preventDefault();

    if (!formRef.current) return;
    setLoading(true);
    setMessage("");

    try {
      await emailjs.sendForm(serviceId, templateId, formRef.current, publicKey);
      formRef.current.reset();

      // Remove message after 3 seconds
      setTimeout(() => setMessage(""), 3000);
    } catch (error) {
      console.error(error);
      setMessage("Failed to send message. Please try again later.");

      // Remove error after 4 seconds
      setTimeout(() => setMessage(""), 4000);
    } finally {
      setLoading(false);
    }
  };

  return (
    <section
      id="contact"
      className="py-20 bg-gradient-to-b from-[#0B0B19] to-[#120A22] text-white px-6 sm:px-10 lg:px-20"
    >
      <div className="max-w-7xl mx-auto rounded-3xl bg-white/5 border border-white/10 backdrop-blur-lg shadow-[0_0_40px_rgba(255,255,255,0.05)] p-8 sm:p-14">
        {/* Heading */}
        <div className="text-center mb-16">
          <motion.h2
            initial={{ opacity: 0, y: -20 }}
            whileInView={{ opacity: 1, y: 0 }}
            transition={{ duration: 0.8 }}
            className="text-4xl sm:text-5xl font-extrabold mb-5 select-none"
          >
            <span className="inline-block mr-3">ðŸ“¨</span>
            <span className="bg-gradient-to-r from-purple-400 to-pink-500 bg-clip-text text-transparent">
              Contact Me
            </span>
          </motion.h2>
          <p className="text-gray-400 max-w-3xl mx-auto text-base sm:text-lg">
            Have a project, opportunity, or just want to say hi? I'd love to hear
            from you!
          </p>
        </div>

        <div className="flex flex-col-reverse md:flex-row items-center gap-12 max-w-6xl mx-auto">
          {/* Contact Form */}
          <motion.div
            initial={{ opacity: 0, x: -30 }}
            whileInView={{ opacity: 1, x: 0 }}
            transition={{ duration: 0.8 }}
            className="w-full md:w-1/2"
          >
            <form
              ref={formRef}
              className="space-y-6 bg-white/5 p-8 rounded-2xl border border-white/10 backdrop-blur-md shadow-[0_0_20px_rgba(255,255,255,0.05)]"
              noValidate
            >
              <input
                type="text"
                name="name"
                placeholder="Your Name"
                required
                className="w-full px-5 py-3 rounded-md border border-white/10 text-white bg-white/10 placeholder-gray-400 focus:outline-none focus:ring-2 focus:ring-purple-500 transition"
                autoComplete="name"
                disabled={loading}
              />
              <input
                type="email"
                name="email"
                placeholder="Your Email"
                required
                className="w-full px-5 py-3 rounded-md bg-white/10 border border-white/10 text-white placeholder-gray-400 focus:outline-none focus:ring-2 focus:ring-purple-500 transition"
                autoComplete="email"
                disabled={loading}
              />
              <textarea
                name="message"
                rows="5"
                placeholder="Your Message"
                required
                className="w-full px-5 py-3 rounded-md bg-white/10 border border-white/10 text-white placeholder-gray-400 focus:outline-none focus:ring-2 focus:ring-purple-500 resize-none transition"
                disabled={loading}
              ></textarea>
              <Button
                type="button"
                onClick={handleAnimatedSubmit}
                className="w-full bg-gradient-to-r from-purple-500 to-pink-500 hover:from-purple-600 hover:to-pink-600 text-white font-semibold py-3 rounded-md transition-all duration-300 cursor-pointer shadow-lg select-none">
                Send Message
              </Button>
              {message && (
                <p
                  className={`mt-3 text-sm ${
                    message.includes("success") ? "text-green-400" : "text-red-400"
                  } select-none`}
                  role="alert"
                >
                  {message}
                </p>
              )}
            </form>
          </motion.div>

          {/* Globe Visual */}
          <motion.div
            initial={{ opacity: 0, x: 30 }}
            whileInView={{ opacity: 1, x: 0 }}
            transition={{ duration: 0.8 }}
            className="w-full md:w-1/2 h-72 sm:h-80 md:h-[28rem] lg:h-[32rem]"
            aria-label="Interactive Globe visualization"
          >
            <Suspense
              fallback={
                <div className="flex justify-center items-center h-full text-white text-lg select-none">
                  Loading Globe...
                </div>
              }
            >
              <World data={sampleArcs} globeConfig={globeConfig} />
            </Suspense>
          </motion.div>
        </div>
      </div>
    </section>
  );
}

export default Contact;